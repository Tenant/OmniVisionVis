bool isValidROI(cv::Rect input, cv::Size region)
{
	if (input.x >= region.width || input.y >= region.height)
		return false;
	if (input.x + input.width < 0 || input.y + input.height < 0)
		return false;
	return true;
}

cv::Rect cutValidROI(cv::Rect input, cv::Size region)
{
	cv::Rect output = input;
	if (output.x < 0)
	{
		output.width += output.x;
		output.x = 0;
	}
	if (output.y < 0)
	{
		output.height+= output.y;
		output.y = 0;
	}
	if (output.x + output.width >= region.width)
		output.width = region.width - 1 - output.x;
	if (output.y + output.height >= region.height)
		output.height = region.height - 1 - output.y;
	return output;
}

cv::Vec3b ColorMap::getColor(double value, double min, double max)
{
	int index = int((value - min) / (max - min) * maxCNum);
	if (index <= 0)
		index = 0;
	else if (index >= maxCNum)
		index = maxCNum - 1;
	return colorTable[index];
}

